#include<iostream>
using namespace std;
int main()
{
    int a,b,c;
    cin>>a>>b>>c;
    if(a+b==c){cout<<"+"<<endl;system("pause");return 0;}
    if(a-b==c){cout<<"-"<<endl;system("pause");return 0;}
    if(a*b==c){cout<<"*"<<endl;system("pause");return 0;}
  cout<<0<<endl;
  
  system("pause");
    return 0;
}
